package main 

import "fmt"

func main(){
	var name string //Variable declaration
	name = "Chika"  // Variable Assignment
	fmt.Println(name)

	name = "Naza"
	fmt.Println(name)

	var age int
	age = 37
	fmt.Println(name, "is",  age, "years old")

	var name2 = "Nursultan" //Variable Declaration and Assignment
	age = 13
	fmt.Println(name2, "is",  age, "years old")

}