package main

import "fmt"

func main() {
	name = "Aruuke"
	fmt.Print("Hello, Naza\n")//Print simply what you pass to it
	fmt.Println("Hello, Naza")//Print what is passed to it, and adds a new line character
	fmt.Printf("Hello, %s", name)//Operates like fmt.Print but allows for output formatting
}

